# Initialize a and b
a, b = 4, 6


# Print the data
print(f"a = {a}")
print(f"b = {b}")


# Bitwise operations
print(f"\na & b\t= {a & b}")
print(f"a | b\t= {a | b}")
print(f"~a\t= {~a}")
print(f"a ^ b\t= {a ^ b}")


# Left and right shift
print(f"\n17 >> 1\t= {17 >> 1}")
print(f"17 >> 1\t= {17 >> 2}")
print(f"19 << 1\t= {17 << 1}")
print(f"21 << 1\t= {17 << 2}")
